package com.mybatis.Entity;

public class bus {
    Integer sub_id;
    String Coin_box;
    String banisters;
    String monitor;
    String indicator;
    String Number_of_layers;
    String compartments;

    public Integer getSub_id() {
        return sub_id;
    }

    public void setSub_id(Integer sub_id) {
        this.sub_id = sub_id;
    }

    public String getCoin_box() {
        return Coin_box;
    }

    public void setCoin_box(String coin_box) {
        Coin_box = coin_box;
    }

    public String getBanisters() {
        return banisters;
    }

    public void setBanisters(String banisters) {
        this.banisters = banisters;
    }

    public String getMonitor() {
        return monitor;
    }

    public void setMonitor(String monitor) {
        this.monitor = monitor;
    }

    public String getIndicator() {
        return indicator;
    }

    public void setIndicator(String indicator) {
        this.indicator = indicator;
    }

    public String getNumber_of_layers() {
        return Number_of_layers;
    }

    public void setNumber_of_layers(String number_of_layers) {
        Number_of_layers = number_of_layers;
    }

    public String getCompartments() {
        return compartments;
    }

    public void setCompartments(String compartments) {
        this.compartments = compartments;
    }
}
